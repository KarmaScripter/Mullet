	 BFY TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 EFY TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 FundCode TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 FundName TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 BudgetAccountCode TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 BudgetAccountName TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 TreasuryAccountCode TEXT(80) NULL DEFAULT NS,DEFAULT NS,
	 TreasuryAccountName TEXT(80) NULL DEFAULT NS,DEFAULT NS,
